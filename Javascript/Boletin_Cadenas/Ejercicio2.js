const devuelvePrimeraLetra = (cadena) => {
    return cadena[0];
}

console.log(devuelvePrimeraLetra("Caramelo"));
console.log(devuelvePrimeraLetra("Antena"));
console.log(devuelvePrimeraLetra("Pelo"));
console.log(devuelvePrimeraLetra("Tarta"));
